const section = document.getElementById("newsSection");

// S√©parer l'article Discord des autres
const discordArticle = articles.find(a => a.specialStyle);
const otherArticles = articles.filter(a => a !== discordArticle);

// Pagination config
const ARTICLES_PER_PAGE = 5;
let currentPage = 1;

// G√©n√®re les pages avec l'article Discord toujours en premier
const paginatedArticles = [];
paginatedArticles.push([discordArticle, ...otherArticles.slice(0, ARTICLES_PER_PAGE - 1)]);

for (let i = ARTICLES_PER_PAGE - 1; i < otherArticles.length; i += ARTICLES_PER_PAGE) {
  paginatedArticles.push(otherArticles.slice(i, i + ARTICLES_PER_PAGE));
}

// Fonction d'affichage d'une page
function renderPage(page) {
  section.innerHTML = ""; // Vide l'ancienne page
  const currentArticles = paginatedArticles[page - 1];

  currentArticles.forEach(article => {
    const articleEl = document.createElement("div");
    articleEl.className = "news-article";

    const content = document.createElement("div");
    content.className = "content";

    const timestamp = document.createElement("div");
    timestamp.className = "timestamp";
    timestamp.textContent = `${article.date} - ${article.time}`;

    const title = document.createElement("h3");
    if(article.iconClass) {
      const icon = document.createElement("i");
      icon.className = article.iconClass + " pinned-icon";
      icon.style.marginRight = "8px";
      icon.style.color = "#5865F2";
      title.appendChild(icon);
    }
    title.appendChild(document.createTextNode(article.title));

    const summary = document.createElement("p");
    summary.innerHTML = article.summary;

    if (article.specialStyle) {
      articleEl.style.backgroundColor = "#2c2f33"; 
      articleEl.style.border = "1px solid #7289da"; 
      articleEl.style.color = "#ffffff"; 
    }

    content.appendChild(timestamp);
    content.appendChild(title);
    content.appendChild(summary);

    if (article.images && article.images.length > 0) {
      const imageWrapper = document.createElement("div");
      imageWrapper.className = "image-wrapper";
      article.images.forEach(src => {
        const link = document.createElement("a");
        link.href = src;
        link.target = "_blank";
        link.rel = "noopener";

        const img = document.createElement("img");
        img.src = src;
        img.alt = article.title;

        link.appendChild(img);
        imageWrapper.appendChild(link);
      });
      content.appendChild(imageWrapper);
    } else if (article.image) {
      const imageWrapper = document.createElement("div");
      imageWrapper.className = "image-wrapper";

      const link = document.createElement("a");
      link.href = article.image;
      link.target = "_blank";
      link.rel = "noopener";

      const img = document.createElement("img");
      img.src = article.image;
      img.alt = article.title;

      link.appendChild(img);
      imageWrapper.appendChild(link);
      content.appendChild(imageWrapper);
    }

    articleEl.appendChild(content);
    section.appendChild(articleEl);
  });

  renderPagination();
}

function renderPagination() {
  const pagination = document.getElementById("pagination");
  if (!pagination) return;

  pagination.innerHTML = "";

  // Bouton pr√©c√©dent (Font Awesome)
  const prevBtn = document.createElement("button");
  prevBtn.className = "pagination-btn";
  prevBtn.disabled = currentPage === 1;

  const prevIcon = document.createElement("i");
  prevIcon.className = "fas fa-angle-left";
  prevBtn.appendChild(prevIcon);

  prevBtn.addEventListener("click", () => {
    if (currentPage > 1) {
      currentPage--;
      renderPage(currentPage);
    }
  });
  pagination.appendChild(prevBtn);

  // Boutons num√©rot√©s
  for (let i = 1; i <= paginatedArticles.length; i++) {
    const btn = document.createElement("button");
    btn.textContent = i;
    btn.className = "pagination-btn";
    if (i === currentPage) btn.classList.add("active");

    btn.addEventListener("click", () => {
      currentPage = i;
      renderPage(currentPage);
    });

    pagination.appendChild(btn);
  }

  // Bouton suivant (Font Awesome)
  const nextBtn = document.createElement("button");
  nextBtn.className = "pagination-btn";
  nextBtn.disabled = currentPage === paginatedArticles.length;

  const nextIcon = document.createElement("i");
  nextIcon.className = "fas fa-angle-right";
  nextBtn.appendChild(nextIcon);

  nextBtn.addEventListener("click", () => {
    if (currentPage < paginatedArticles.length) {
      currentPage++;
      renderPage(currentPage);
    }
  });
  pagination.appendChild(nextBtn);
}


// Initialisation
renderPage(currentPage);
